package gr.aueb.cf.cf9.ch1;

/**
 * The purpose of the class is to write to the console
 * the result of the addition of 2 integers (num1, num2)
 * using the variable (result)
 * initial value of num1 = 7
 * initial value of num2 = 12
 * expected result = 19

 * @author Iraklis Kalamas
 * @version 1.0
 */
public class SumTwoNumbers {
    public static void main(String[] args) {
        int num1 = 7;
        int num2 = 12;
        int result = num1 + num2;
        System.out.println("Το αποτέλεσμα της πρόσθεσης είναι ίσο με " + result);
    }
}
